def using_include(array, element)
  array.include?(element)
end

def using_sort(array)
new_array = array.sort
return new_array
end

def using_reverse(array)
new_array = array.reverse
return new_array
end

def using_first(array)
var = array.first
return var
end

def using_last(array)
var = array.last
return var
end

def using_size(array)
var = array.size
return var
end
